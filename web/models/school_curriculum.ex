defmodule SchoolAgenda.SchoolCurriculum do
  use SchoolAgenda.Web, :model

  schema "school_curriculums" do
    field :title, :string

    timestamps()
  end

  @doc """
  Builds a changeset based on the `struct` and `params`.
  """
  def changeset(struct, params \\ %{}) do
    struct
    |> cast(params, [:title])
    |> validate_required([:title])
  end
end
